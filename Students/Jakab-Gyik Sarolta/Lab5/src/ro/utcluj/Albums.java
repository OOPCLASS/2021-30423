package ro.utcluj;

public class Albums {
    public String title;
    public String artist;
    public int year;
    public int quantity;
    public double price;

    public Albums(String title, String artist, int year, int quantity, double price) {
        this.title = title;
        this.artist = artist;
        this.year = year;
        this.quantity = quantity;
        this.price = price;
    }

}
